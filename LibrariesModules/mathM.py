import math

#Write a Python script to demonstrate the use of the math module.
# Calculate the square root of 16
sqrt_16 = math.sqrt(16)
print(f"The square root of 16 is {sqrt_16}")

# Calculate the factorial of 5
factorial_5 = math.factorial(5)
print(f"The factorial of 5 is {factorial_5}")

# Calculate the greatest common divisor of 48 and 64
gcd_48_64 = math.gcd(48, 64)
print(f"The greatest common divisor of 48 and 64 is {gcd_48_64}")

# Calculate the sine of pi/2
sine_pi_over_2 = math.sin(math.pi / 2)
print(f"The sine of pi/2 is {sine_pi_over_2}")

# Calculate the logarithm base 10 of 1000
log10_1000 = math.log10(1000)
print(f"The logarithm base 10 of 1000 is {log10_1000}")